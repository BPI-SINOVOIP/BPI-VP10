void usMBCRCInit(void)
{
	set_csr(CRC_CR, CRCVAL);	//0-->0x0000  1-->0xffff
	set_csr(CRC_CR, CRCDINI);	//1-->init success
	set_csr(CRC_CR, RESINV);	//1-->Invert output result
	set_csr(CRC_CR, DATINV);	//1-->Invert data input
	set_csr(CRC_CR, CRCSEL);	//0-->0x1021  1-->0x8005

#if CRC_DMA_ENABLED
	write_csr(CRC_DMA_CR, DMA_CRC_Send);
	set_csr(CRC_DMA_CR, DMAEN);
#endif
}

USHORT
	usMBCRC16(UCHAR * pucFrame, USHORT usLen)
{
#if CRC_DMA_ENABLED
	USHORT crcresult;
	
	set_csr(CRC_CR, CRCDINI);	//1-->init success
	
	write_csr(CRC_DMA_LEN, usLen - 1);
	write_csr(CRC_DMA_BA, pucFrame);
	set_csr(CRC_DMA_CR, DMABSY);
	while (readbit_csr(CRC_DMA_CR, DMABSY));
	crcresult = read_csr(CRC_DR);

	return crcresult;

#else
	
	USHORT crcresult;
	set_csr(CRC_CR, CRCDINI);	//1-->init success	
	while (usLen--)
	{
		write_csr(CRC_DIN, *(pucFrame++));
	}
	crcresult = read_csr(CRC_DR);

	return crcresult;
#endif // #if CRC_DMA_ENABLED
}


USHORT
	usMBCRCSum(SendFramePointerTypeDef FramePointer)
{
#if CRC_DMA_ENABLED
	USHORT crcresult;
	USHORT OffsetCount = 0;
	USHORT AddrCount = FramePointer.Count - 1;
	UCHAR * pucFrame;
	USHORT i = 0, IncLen;
	
	set_csr(CRC_CR, CRCDINI);	//1-->init success

	while (AddrCount--)
	{
		OffsetCount = FramePointer.Length[i];
		pucFrame = FramePointer.Addr[i++];
		while (OffsetCount)
		{
			IncLen = OffsetCount;
			if (IncLen > DMA_MAXLEN)
				IncLen = DMA_MAXLEN;
			
			write_csr(CRC_DMA_LEN, IncLen - 1);
			write_csr(CRC_DMA_BA, pucFrame);
			set_csr(CRC_DMA_CR, DMABSY);
			while (readbit_csr(CRC_DMA_CR, DMABSY));
			
			OffsetCount -= IncLen;
			pucFrame += IncLen;
		}
	}
	crcresult = read_csr(CRC_DR);
	return crcresult;
	
#else	
	
	USHORT wCRCin = 0;
	USHORT OffsetCount = 0;
	USHORT i = 0;
	USHORT AddrCount = FramePointer.Count - 1;
	UCHAR * pucFrame;

	set_csr(CRC_CR, CRCDINI);	//1-->init success
	while (AddrCount--)
	{
		OffsetCount = FramePointer.Length[i];
		pucFrame = FramePointer.Addr[i++];

		while (OffsetCount--)
		{
			write_csr(CRC_DIN, *(pucFrame++));
		}
	}
	wCRCin = read_csr(CRC_DR);
	return (wCRCin);
#endif // #if CRC_DMA_ENABLED
}


